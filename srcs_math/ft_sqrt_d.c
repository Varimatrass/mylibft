#include "libft.h"

double	ft_sqrt_d(t_uint n)
{
	double	res;
	t_uint	s;

	if (!n)
		return (0);
	s = ft_sqrt(n);
	res = (s * s) * ((s * s) + 6 * n) + (n * n);
	res /= (4 * s) * ((s * s) + n);
	return (res);
}
